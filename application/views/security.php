                        <p>The base media for ButtonOS is read-only. This provides Physical protection from undesirable modification.</p>

                        <h1>Built-in Firewall</h1>
                        <p>ButtonOS has built-in to the base a firewall that prevents incoming connections.</p>

                        <h1>Standard Components</h1>
                        <p>The system is composed of components that are constantly being monitored by security experts the world over.</p>


